package Day03.com.ict.edu;

public class Ex06_oper {

	public static void main(String[] args) {
		//대입연산: =, +=, -=, *=, /=, %=
		//A = B -> 변수 A에 데이터 B를 저장하라
		//A += B -> 저장된 변수 A값이랑 B를 더해서 변수A에 저장하라
		
		int su1 = 7;
		su1 += 4; //su1 = su1 + 4
		System.out.println(su1);
		
		su1 -= 4; //su1 = su1 - 4
		System.out.println(su1);
			
	}

}
